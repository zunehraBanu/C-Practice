/*2. Write a program in C++ to print the sum of two numbers.
The sum of 29 and 30 is : 59*/
#include <iostream>
using namespace std;
int main()
{
    // Declare and initialize two numbers
    int num1 = 29;
    int num2 = 30;
    // Calculate the sum of num1 and num2
    int sum = num1 + num2;
    // Display the result
    cout << "The sum of " << num1 << " and " << num2 << " is : " << sum;
    return 0;
}